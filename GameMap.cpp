// Метод для завантаження карти
bool GameMap::generateMap(const std::string& tileset, int* tiles, unsigned int width, unsigned int height) {
    m_vertices.clear(); // Очистка старих вершин
    wallTiles.clear(); // Очистка вектора стін 
    // Завантажуємо тайлсет
    if (!m_tileset.loadFromFile(tileset))
        return false;
    sf::Vector2u tileSize = { 16, 16 };
    // Масштабуємо тайли
    float scale = 3.5f;

    // Налаштовуємо вершини для кожного тайла
    m_vertices.setPrimitiveType(sf::Quads);
    m_vertices.resize(width * height * 4);
    
    for (unsigned int i = 0; i < width; ++i) {
        for (unsigned int j = 0; j < height; ++j) {
            int tileNumber = tiles[i + j * width];
            // Перевіряємо, чи є тайл стіною (значення 1)
            if (tileNumber == 1 || tileNumber == 2) {
                // Додаємо індекс цього тайла до вектора стін
                addWallTile(i + j * width);
            }

            // Обчислюємо текстурні координати тайла
            int tu = tileNumber % (m_tileset.getSize().x / tileSize.x);
            int tv = tileNumber / (m_tileset.getSize().x / tileSize.x);

            // Отримуємо вказівник на відповідний квадрант вершин
            sf::Vertex* quad = &m_vertices[(i + j * width) * 4];

            // Встановлюємо позиції для кожного тайла з масштабуванням
            quad[0].position = sf::Vector2f(i * tileSize.x * scale, j * tileSize.y * scale);
            quad[1].position = sf::Vector2f((i + 1) * tileSize.x * scale, j * tileSize.y * scale);
            quad[2].position = sf::Vector2f((i + 1) * tileSize.x * scale, (j + 1) * tileSize.y * scale);
            quad[3].position = sf::Vector2f(i * tileSize.x * scale, (j + 1) * tileSize.y * scale);

            // Встановлюємо текстурні координати без зміщення
            quad[0].texCoords = sf::Vector2f(tu * tileSize.x, tv * tileSize.y);
            quad[1].texCoords = sf::Vector2f((tu + 1) * tileSize.x, tv * tileSize.y);
            quad[2].texCoords = sf::Vector2f((tu + 1) * tileSize.x, (tv + 1) * tileSize.y);
            quad[3].texCoords = sf::Vector2f(tu * tileSize.x, (tv + 1) * tileSize.y);
        }
    }

    return true;
}

void GameMap::draw(sf::RenderTarget& target, sf::RenderStates states) const {
    // Додаємо текстуру
    states.texture = &m_tileset;
    // Малюємо тайли
    target.draw(m_vertices, states);
}

int GameMap::getTileIndex(const sf::Sprite& player) const {
    // Отримуємо позицію гравця
    sf::Vector2f playerPos = player.getPosition();

    // Оригінальні розміри текстури тайлів
    float tileWidth = tileSize.x * 3.5f;  // Враховуємо збільшення масштабу в 3.5 рази
    float tileHeight = tileSize.y * 3.5f; // Враховуємо збільшення масштабу в 3.5 рази

    // Обчислюємо індекс тайла, враховуючи збільшення
    int tileX = static_cast<int>(playerPos.x / tileWidth);
    int tileY = static_cast<int>(playerPos.y / tileHeight);

    // Перевірка на вихід за межі масиву
    if (tileX < 0 || tileX >= cols || tileY < 0 || tileY >= rows) {
        return -1; // Позначаємо, що гравець поза межами карти
    }

    // Обчислюємо індекс в масиві тайлів
    int index = tileY * cols + tileX;

    // Перевірка на існування індексу в масиві тайлів
    if (index < 0 || index >= 2800) {
        return -1; // Позначаємо, що індекс некоректний
    }

    return index; // Повертаємо індекс тайла
}

const sf::Texture* GameMap::getPlayerTileTexture(const sf::Sprite& player) const {
    int tileIndex = getTileIndex(player);  // Отримуємо індекс тайла, на якому стоїть гравець

    // Перевірка чи індекс дійсний
    if (tileIndex == -1 || tileIndex >= tileTextures.size()) {
        return nullptr; // Якщо індекс недійсний, повертаємо nullptr
    }

    // Повертаємо вказівник на текстуру тайла
    return tileTextures[tileIndex]; // Повертаємо вказівник на текстуру (const sf::Texture*)
}

// Метод для додавання тайлів-стін (викликається при ініціалізації карти)
void GameMap::addWallTile(int tileIndex) {
    wallTiles.push_back(tileIndex);
}

// Метод для перевірки, чи є тайл стіною
bool GameMap::isWallTile(int tileIndex) const {
    return std::find(wallTiles.begin(), wallTiles.end(), tileIndex) != wallTiles.end();
}
