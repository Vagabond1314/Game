int* GameMap::loadGameMapfromtxt(const std::string& filename) {
    std::ifstream file(filename);

    if (!file.is_open()) {
        std::cerr << "Помилка: Не вдалося відкрити файл!" << std::endl;
        return nullptr;
    }

    std::vector<int> tempMap; // Тимчасовий вектор для зберігання елементів
    rows = 0;
    cols = 0;

    std::string line;
    while (std::getline(file, line)) {
        std::istringstream lineStream(line);
        int value;
        int currentCols = 0;

        // Зчитуємо числа, розділені комами
        while (lineStream >> value) {
            tempMap.push_back(value);
            ++currentCols;
            if (lineStream.peek() == ',') {
                lineStream.ignore();
            }
        }

        // Перевіряємо кількість стовпців у першому рядку
        if (rows == 0) {
            cols = currentCols;
        }
        else if (currentCols != cols) {
            std::cerr << "Помилка: Неконсистентна кількість стовпців у файлі!" << std::endl;
            return nullptr;
        }

        ++rows; // Збільшуємо кількість рядків
    }

    file.close();

    // Створюємо динамічний масив і копіюємо дані
    int* map = new int[tempMap.size()];
    std::copy(tempMap.begin(), tempMap.end(), map);

    std::cout << "Карта завантажена успішно. Розмір: " << rows << "x" << cols << std::endl;
    return map;
}

